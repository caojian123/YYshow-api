package com.yyw.test.domain;

public class ModuleStyle {
	private String moduleId;
	private String width;
	private String height;
	private String left;
	private String top;
	private String fontSize;
	private String color;
	private String zIndex;
	private String animationDelay;
	private String animationIterationCount;
	
	
	public String getModuleId() {
		return moduleId;
	}

	public void setModuleId(String moduleId) {
		this.moduleId = moduleId;
	}

	public String getWidth() {
		return width;
	}

	public void setWidth(String width) {
		this.width = width;
	}

	public String getHeight() {
		return height;
	}

	public void setHeight(String height) {
		this.height = height;
	}

	public String getLeft() {
		return left;
	}

	public void setLeft(String left) {
		this.left = left;
	}

	public String getTop() {
		return top;
	}

	public void setTop(String top) {
		this.top = top;
	}

	public String getFontSize() {
		return fontSize;
	}

	public void setFontSize(String fontSize) {
		this.fontSize = fontSize;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public String getzIndex() {
		return zIndex;
	}

	public void setzIndex(String zIndex) {
		this.zIndex = zIndex;
	}

	public String getAnimationDelay() {
		return animationDelay;
	}

	public void setAnimationDelay(String animationDelay) {
		this.animationDelay = animationDelay;
	}

	public String getAnimationIterationCount() {
		return animationIterationCount;
	}

	public void setAnimationIterationCount(String animationIterationCount) {
		this.animationIterationCount = animationIterationCount;
	}

}
